COMMODITY_KEYWORDS = ["原油", "豆粕", "白银", "商品", "能源化工品"]


def classify(name: str, index_name: str = "") -> str:
    """给定 ETF 简称（及可选拟合指数名），返回行业/主题标签。"""
    text = f"{name} {index_name}"

    for industry, kws in INDUSTRY_RULES:
        for kw in kws:
            if kw in text:
                return industry

    for kw in COMMODITY_KEYWORDS:
        if kw in text:
            return "商品"
    for kw in OVERSEAS_KEYWORDS:
        if kw in text:
            return "海外QDII"
    for kw in BROAD_KEYWORDS:
        if kw in text:
            return "宽基"

    return "其他主题"


